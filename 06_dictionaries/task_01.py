"""
Create a dictionary

student = {
    "name": "John",
    "age": 20,
    "score": 95
}

Print:

Name: John
Age: 20
Score: 95
"""

student = {
    "name": "John",
    "age": 20,
    "grade": 95
}

print(f"Name: {student['name']}")
print(f"Age: {student['age']}")
print(f"Grade: {student['score']}")